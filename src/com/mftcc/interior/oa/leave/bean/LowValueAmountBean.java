package com.mftcc.interior.oa.leave.bean;

public class LowValueAmountBean {
	
	private String lowValueNo;
	private String lowValueName;
	private String lowValueDate;
	private String lowValueAmount;
	public String getLowValueNo() {
		return lowValueNo;
	}
	public void setLowValueNo(String lowValueNo) {
		this.lowValueNo = lowValueNo;
	}
	public String getLowValueName() {
		return lowValueName;
	}
	public void setLowValueName(String lowValueName) {
		this.lowValueName = lowValueName;
	}
	public String getLowValueDate() {
		return lowValueDate;
	}
	public void setLowValueDate(String lowValueDate) {
		this.lowValueDate = lowValueDate;
	}
	public String getLowValueAmount() {
		return lowValueAmount;
	}
	public void setLowValueAmount(String lowValueAmount) {
		this.lowValueAmount = lowValueAmount;
	}
	
	
}
